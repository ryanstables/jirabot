import { Router, json } from 'express';
import { createAppAuth } from '@octokit/auth-app';
import { writeFileSync } from 'fs';
import { randomBytes } from 'node:crypto';
import path from 'path';

export function createSetupRouter(rootDir: string): Router {
  const router = Router();
  router.use(json());

  router.get('/', (_req, res) => {
    res.setHeader('Content-Type', 'text/html; charset=utf-8');
    res.send(WIZARD_HTML);
  });

  router.get('/api/generate-secret', (_req, res) => {
    res.json({ secret: randomBytes(32).toString('hex') });
  });

  router.post('/api/validate-jira', async (req, res) => {
    const { host, email, apiToken } = req.body as { host: string; email: string; apiToken: string };
    try {
      const response = await fetch('https://' + host + '/rest/api/3/myself', {
        headers: {
          'Authorization': 'Basic ' + Buffer.from(email + ':' + apiToken).toString('base64'),
          'Accept': 'application/json',
        },
      });
      if (!response.ok) {
        const text = await response.text();
        return res.json({ ok: false, error: 'Jira returned ' + response.status + ': ' + text.slice(0, 200) });
      }
      const data = await response.json() as { accountId: string; displayName: string };
      return res.json({ ok: true, accountId: data.accountId, displayName: data.displayName });
    } catch (err) {
      return res.json({ ok: false, error: String(err) });
    }
  });

  router.post('/api/validate-github-pat', async (req, res) => {
    const { pat } = req.body as { pat: string };
    try {
      const response = await fetch('https://api.github.com/user', {
        headers: {
          'Authorization': 'token ' + pat,
          'User-Agent': 'JiraBot-Setup',
          'Accept': 'application/vnd.github.v3+json',
        },
      });
      if (!response.ok) {
        return res.json({ ok: false, error: 'GitHub returned ' + response.status + ' — check the token and that the "repo" scope is selected.' });
      }
      const data = await response.json() as { login: string };
      return res.json({ ok: true, login: data.login });
    } catch (err) {
      return res.json({ ok: false, error: String(err) });
    }
  });

  router.post('/api/validate-github-app', async (req, res) => {
    const { appId, privateKey, installationId } = req.body as {
      appId: string; privateKey: string; installationId: string;
    };
    try {
      const auth = createAppAuth({
        appId: Number(appId),
        privateKey: privateKey.replace(/\\n/g, '\n'),
        installationId: Number(installationId),
      });
      await auth({ type: 'installation' });
      return res.json({ ok: true });
    } catch (err) {
      return res.json({ ok: false, error: String(err) });
    }
  });

  router.post('/api/validate-anthropic', async (req, res) => {
    const { apiKey } = req.body as { apiKey: string };
    try {
      const response = await fetch('https://api.anthropic.com/v1/models', {
        headers: { 'x-api-key': apiKey, 'anthropic-version': '2023-06-01' },
      });
      if (!response.ok) {
        return res.json({ ok: false, error: 'Anthropic returned ' + response.status + ' — check the key is valid and not expired.' });
      }
      return res.json({ ok: true });
    } catch (err) {
      return res.json({ ok: false, error: String(err) });
    }
  });

  router.post('/api/save', (req, res) => {
    const { jira, github, anthropicApiKey, boards, slack } = req.body as {
      jira: { host: string; email: string; apiToken: string; accountId: string; webhookSecret: string };
      github: { mode: string; pat?: string; appId?: string; privateKey?: string; installationId?: string };
      anthropicApiKey: string;
      boards: Array<{ jiraProject: string; githubRepo: string; defaultBranch: string; targetStatus: string; escalationStatus: string }>;
      slack?: { botToken: string; signingSecret: string };
    };
    try {
      const lines: string[] = ['# Generated by JiraBot setup wizard', ''];
      lines.push('# Jira');
      lines.push('JIRA_HOST=' + jira.host);
      lines.push('JIRA_AGENT_EMAIL=' + jira.email);
      lines.push('JIRA_API_TOKEN=' + jira.apiToken);
      lines.push('JIRA_AGENT_ACCOUNT_ID=' + jira.accountId);
      lines.push('JIRA_WEBHOOK_SECRET=' + jira.webhookSecret);
      lines.push('');
      lines.push('# GitHub');
      if (github.mode === 'pat') {
        lines.push('GITHUB_PAT=' + (github.pat ?? ''));
      } else {
        lines.push('GITHUB_APP_ID=' + (github.appId ?? ''));
        lines.push('GITHUB_APP_PRIVATE_KEY=' + (github.privateKey ?? '').replace(/\n/g, '\\n'));
        lines.push('GITHUB_APP_INSTALLATION_ID=' + (github.installationId ?? ''));
      }
      lines.push('');
      lines.push('# Anthropic');
      lines.push('ANTHROPIC_API_KEY=' + anthropicApiKey);
      lines.push('');
      lines.push('# Redis (Docker Compose overrides this automatically)');
      lines.push('REDIS_URL=redis://localhost:6379');
      lines.push('');
      lines.push('# Board mapping');
      lines.push('BOARDS_CONFIG=' + JSON.stringify(boards));
      if (slack?.botToken) {
        lines.push('');
        lines.push('# Slack');
        lines.push('SLACK_BOT_TOKEN=' + slack.botToken);
        lines.push('SLACK_SIGNING_SECRET=' + slack.signingSecret);
      }
      lines.push('');
      writeFileSync(path.join(rootDir, '.env'), lines.join('\n'), 'utf8');
      return res.json({ ok: true });
    } catch (err) {
      return res.json({ ok: false, error: String(err) });
    }
  });

  return router;
}

