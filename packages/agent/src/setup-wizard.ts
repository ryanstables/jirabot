g('btn-verify-jira').addEventListener('click', function() {
  var host = g('jira-host').value.trim().replace(/^https?:\/\//, '');
  var email = g('jira-email').value.trim();
  var token = g('jira-token').value.trim();
  if (!host || !email || !token) { showMsg('msg-jira', 'error', 'Fill in all three fields before verifying.'); return; }
  setBtn('btn-verify-jira', 'Verifying&#8230;', true);
  fetch('/setup/api/validate-jira', {
    method: 'POST', headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ host: host, email: email, apiToken: token }),
  }).then(function(r) { return r.json(); }).then(function(d) {
    setBtn('btn-verify-jira', 'Verify connection', false);
    if (d.ok) {
      savedState.jiraDisplayName = d.displayName;
      savedState.jiraAccountId = d.accountId;
      verified[0] = true;
      showMsg('msg-jira', 'success', 'Connected as <strong>' + d.displayName + '</strong> &mdash; account ID: <code>' + d.accountId + '</code>');
      // Fetch webhook secret
      fetch('/setup/api/generate-secret').then(function(r2) { return r2.json(); }).then(function(s) { savedState.jiraWebhookSecret = s.secret; });
    } else {
      verified[0] = false;
      showMsg('msg-jira', 'error', d.error || 'Connection failed.');
    }
    goTo(step); // refresh sidebar
  }).catch(function(e) { setBtn('btn-verify-jira', 'Verify connection', false); showMsg('msg-jira', 'error', String(e)); });
});

// ─── GitHub verify ────────────────────────────────────────────
g('btn-verify-gh').addEventListener('click', function() {
  setBtn('btn-verify-gh', 'Verifying&#8230;', true);
  var endpoint, payload;
  if (ghMode === 'pat') {
    var pat = g('gh-pat').value.trim();
    if (!pat) { setBtn('btn-verify-gh', 'Verify connection', false); showMsg('msg-gh', 'error', 'Enter a token first.'); return; }
    endpoint = '/setup/api/validate-github-pat';
    payload = { pat: pat };
  } else {
    var appId = g('gh-app-id').value.trim();
    var pem = g('gh-pem').value.trim();
    var instId = g('gh-install-id').value.trim();
    if (!appId || !pem || !instId) { setBtn('btn-verify-gh', 'Verify connection', false); showMsg('msg-gh', 'error', 'Fill in App ID, private key, and Installation ID.'); return; }
    endpoint = '/setup/api/validate-github-app';
    payload = { appId: appId, privateKey: pem, installationId: instId };
  }
  fetch(endpoint, {
    method: 'POST', headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify(payload),
  }).then(function(r) { return r.json(); }).then(function(d) {
    setBtn('btn-verify-gh', 'Verify connection', false);
    if (d.ok) {
      savedState.githubLogin = d.login || '';
      verified[1] = true;
      var who = ghMode === 'pat' ? ('Connected as <strong>' + d.login + '</strong>') : 'GitHub App authenticated successfully.';
      showMsg('msg-gh', 'success', who);
    } else {
      verified[1] = false;
      showMsg('msg-gh', 'error', d.error || 'Authentication failed.');
    }
    goTo(step);
  }).catch(function(e) { setBtn('btn-verify-gh', 'Verify connection', false); showMsg('msg-gh', 'error', String(e)); });
});

