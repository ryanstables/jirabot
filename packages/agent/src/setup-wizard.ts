function getBoards() {
  var out = [];
  document.querySelectorAll('.board-card').forEach(function(card) {
    var proj = card.querySelector('[name=proj]').value.trim().toUpperCase();
    var repo = card.querySelector('[name=repo]').value.trim();
    if (!proj || !repo) return;
    out.push({
      jiraProject: proj,
      githubRepo: repo,
      defaultBranch: card.querySelector('[name=branch]').value.trim() || 'main',
      targetStatus: card.querySelector('[name=target]').value.trim() || 'Ready for Review',
      escalationStatus: card.querySelector('[name=escalation]').value.trim() || 'Needs Clarity',
    });
  });
  return out;
}

// ─── Review ──────────────────────────────────────────────────
function buildReview() {
  var host = g('jira-host').value.trim().replace(/^https?:\/\//, '');
  var boards = getBoards();
  var slackOn = g('slack-toggle').checked;
  var ghDesc = ghMode === 'pat'
    ? 'PAT (' + (savedState.githubLogin || 'authenticated') + ')'
    : 'App ID ' + (g('gh-app-id').value.trim() || '?');
  var boardDesc = boards.length ? boards.map(function(b) { return b.jiraProject + ' &#8594; ' + b.githubRepo; }).join(', ') : 'None configured';
  var rows = [
    ['&#10003;', 'Jira', host + ' &mdash; ' + (savedState.jiraDisplayName || 'connected')],
    ['&#10003;', 'GitHub', ghDesc],
    ['&#10003;', 'Anthropic', 'Key configured'],
    [boards.length ? '&#10003;' : '&#9888;', 'Boards', boardDesc],
    [slackOn ? '&#10003;' : '&mdash;', 'Slack', slackOn ? 'Enabled' : 'Disabled'],
  ];
  g('review-rows').innerHTML = rows.map(function(r) {
    return '<div class="review-row"><span class="review-check">' + r[0] + '</span><span class="review-key">' + r[1] + '</span><span class="review-val">' + r[2] + '</span></div>';
  }).join('');
  // Webhook block
  var secret = savedState.jiraWebhookSecret || '(generated on save)';
  g('webhook-block').innerHTML =
    '<div><span class="wk">URL:    </span><span class="wv">https://&lt;ngrok-url&gt;/webhook/jira</span></div>' +
    '<div><span class="wk">Secret: </span><span class="wv">' + secret + '</span></div>' +
    '<div><span class="wk">Events: </span><span class="wv">Issue &#8594; updated</span></div>';
}

